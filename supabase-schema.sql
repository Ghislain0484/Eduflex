-- 5. COMPLETED CHAPTERS TABLE (For precise progress tracking)
create table if not exists public.completed_chapters (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid(),
  course_id bigint not null references public.courses(id) on delete cascade,
  chapter_id bigint not null references public.chapters(id) on delete cascade,
  completed_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique (user_id, chapter_id)
);

-- Enable Row Level Security (RLS) for completed_chapters
alter table public.completed_chapters enable row level security;

-- Policies for completed_chapters
create policy "Allow users to read their own completions"
  on public.completed_chapters for select
  using (auth.uid() = user_id);

-- Let users insert or delete completions for themselves
create policy "Allow users to modify their own completions"
  on public.completed_chapters for all
  using (auth.uid() = user_id);


-- 6. PROGRESS TRIGGER (Calculates progression based on completed chapters)
create or replace function public.calculate_enrollment_progress()
returns trigger as $$
declare
  v_user_id uuid;
  v_course_id bigint;
  v_total_chapters integer;
  v_completed_chapters integer;
  v_progress integer;
begin
  if (TG_OP = 'DELETE') then
    v_user_id := old.user_id;
    v_course_id := old.course_id;
  else
    v_user_id := new.user_id;
    v_course_id := new.course_id;
  end if;

  -- Count total chapters
  select count(*) into v_total_chapters
  from public.chapters
  where course_id = v_course_id;

  -- Count completed chapters
  select count(*) into v_completed_chapters
  from public.completed_chapters
  where user_id = v_user_id and course_id = v_course_id;

  -- Calculate percentage
  if v_total_chapters > 0 then
    v_progress := (v_completed_chapters * 100) / v_total_chapters;
  else
    v_progress := 0;
  end if;

  -- Update enrollment progress
  update public.enrollments
  set progress_percent = v_progress
  where user_id = v_user_id and course_id = v_course_id;

  return null;
end;
$$ language plpgsql security definer;

-- Trigger on completed_chapters
create or replace trigger on_chapter_completion_change
  after insert or delete on public.completed_chapters
  for each row execute procedure public.calculate_enrollment_progress();


