-- 3. CHAPTERS TABLE
create table if not exists public.chapters (
  id bigint generated by default as identity primary key,
  course_id bigint not null references public.courses(id) on delete cascade,
  title text not null,
  content text,
  video_url text,
  sort_order integer not null default 0,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable Row Level Security (RLS) for chapters
alter table public.chapters enable row level security;

-- Policies for chapters
create policy "Allow public read access for chapters"
  on public.chapters for select
  using (true);

create policy "Allow course owners to write chapters"
  on public.chapters for all
  using (
    exists (
      select 1 from public.courses
      where id = course_id and user_id = auth.uid()
    )
  );


